package com.ml.tj.bean;

public class Medication {

	private String medication;
	private String instructions;
	private String status;
	private String remainingRefills;
	private String lastFilledOn;
	private String initiallyOrderedOn;
	private String quantity;
	private String daysSupply;
	private String pharmacy;
	private String prescriptionNbr;
	public String getMedication() {
		return medication;
	}
	public void setMedication(String medication) {
		this.medication = medication;
	}
	public String getInstructions() {
		return instructions;
	}
	public void setInstructions(String instructions) {
		this.instructions = instructions;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getRemainingRefills() {
		return remainingRefills;
	}
	public void setRemainingRefills(String remainingRefills) {
		this.remainingRefills = remainingRefills;
	}
	public String getLastFilledOn() {
		return lastFilledOn;
	}
	public void setLastFilledOn(String lastFilledOn) {
		this.lastFilledOn = lastFilledOn;
	}
	public String getInitiallyOrderedOn() {
		return initiallyOrderedOn;
	}
	public void setInitiallyOrderedOn(String initiallyOrderedOn) {
		this.initiallyOrderedOn = initiallyOrderedOn;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getDaysSupply() {
		return daysSupply;
	}
	public void setDaysSupply(String daysSupply) {
		this.daysSupply = daysSupply;
	}
	public String getPharmacy() {
		return pharmacy;
	}
	public void setPharmacy(String pharmacy) {
		this.pharmacy = pharmacy;
	}
	public String getPrescriptionNbr() {
		return prescriptionNbr;
	}
	public void setPrescriptionNbr(String prescriptionNbr) {
		this.prescriptionNbr = prescriptionNbr;
	}
	
	
	
	
}
